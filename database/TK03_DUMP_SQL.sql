-- 1. TABEL PENGGUNA (USERS)
CREATE TABLE Users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    role VARCHAR(20) NOT NULL CHECK (role IN ('Admin', 'Organizer', 'Customer')),
    username VARCHAR(50) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    full_name VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE,
    phone VARCHAR(20)
);


-- 2. TABEL PROMOSI (PROMOTIONS)
CREATE TABLE Promotions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    promo_code VARCHAR(50) UNIQUE NOT NULL,
    discount_type VARCHAR(20) NOT NULL CHECK (discount_type IN ('Persentase', 'Nominal')),
    discount_value DECIMAL(10, 2) NOT NULL CHECK (discount_value > 0),
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    usage_limit INT NOT NULL CHECK (usage_limit > 0),
    used_count INT DEFAULT 0,
    CONSTRAINT check_dates CHECK (end_date >= start_date)
);

-- 3. TABEL EVENT
CREATE TABLE Events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(150) NOT NULL,
    capacity INT NOT NULL,
    location VARCHAR(200) NOT NULL,
    event_date TIMESTAMP NOT NULL,
    has_reserved_seating BOOLEAN DEFAULT FALSE,
    organizer_id UUID REFERENCES Users(id) ON DELETE CASCADE
);

-- 4. TABEL KATEGORI TIKET
CREATE TABLE Ticket_Categories (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    event_id UUID REFERENCES Events(id) ON DELETE CASCADE,
    name VARCHAR(100) NOT NULL,
    quota INT NOT NULL CHECK (quota > 0),
    price DECIMAL(15, 2) NOT NULL,
    booked INT DEFAULT 0
);

-- 5. TABEL KURSI (SEATS)
CREATE TABLE Seats (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    event_id UUID REFERENCES Events(id) ON DELETE CASCADE,
    seat_number VARCHAR(10) NOT NULL,
    is_available BOOLEAN DEFAULT TRUE
);

-- 6. TABEL ORDER
CREATE TABLE Orders (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    payment_status VARCHAR(20) NOT NULL CHECK (payment_status IN ('Pending', 'Lunas', 'Dibatalkan')),
    total_amount DECIMAL(15, 2) NOT NULL,
    customer_id UUID REFERENCES Users(id) ON DELETE CASCADE,
    event_id UUID REFERENCES Events(id) ON DELETE CASCADE,
    promo_id UUID REFERENCES Promotions(id) ON DELETE SET NULL
);

-- 7. TABEL ARTIS (ARTISTS)
CREATE TABLE Artists (
    artist_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(100) NOT NULL,
    genre VARCHAR(100)
);

--Tabel USER_ACCOUNT
CREATE TABLE USER_ACCOUNT (
    user_id UUID PRIMARY KEY,
    username VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL
);

--Tabel ROLE
CREATE TABLE ROLE (
    role_id UUID PRIMARY KEY,
    role_name VARCHAR(50) UNIQUE NOT NULL
);

--Tabel ACCOUNT_ROLE
CREATE TABLE ACCOUNT_ROLE (
    role_id UUID REFERENCES ROLE(role_id),
    user_id UUID REFERENCES USER_ACCOUNT(user_id),
    PRIMARY KEY (role_id, user_id)
);

--Tabel CUSTOMER
CREATE TABLE CUSTOMER (
    customer_id UUID PRIMARY KEY,
    full_name VARCHAR(100) NOT NULL,
    phone_number VARCHAR(20),
    user_id UUID UNIQUE NOT NULL REFERENCES USER_ACCOUNT(user_id)
);

--Tabel ORGANIZER
CREATE TABLE ORGANIZER (
    organizer_id UUID PRIMARY KEY,
    organizer_name VARCHAR(100) NOT NULL,
    contact_email VARCHAR(100),
    user_id UUID UNIQUE NOT NULL REFERENCES USER_ACCOUNT(user_id)
);

--Tabel ORDER
CREATE TABLE "ORDER" (
    order_id UUID PRIMARY KEY,
    order_date TIMESTAMP NOT NULL,
    payment_status VARCHAR(20) NOT NULL,
    total_amount NUMERIC(12,2) NOT NULL CHECK (total_amount >= 0),
    customer_id UUID NOT NULL REFERENCES CUSTOMER(customer_id)
);

--Tabel PROMOTION
CREATE TABLE PROMOTION (
    promotion_id UUID PRIMARY KEY,
    promo_code VARCHAR(50) UNIQUE NOT NULL,
    discount_type VARCHAR(20) NOT NULL CHECK (discount_type IN ('NOMINAL', 'PERCENTAGE')),
    discount_value NUMERIC(12,2) NOT NULL CHECK (discount_value > 0),
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    usage_limit INTEGER NOT NULL CHECK (usage_limit > 0)
);

--Tabel ORDER_PROMOTION
CREATE TABLE ORDER_PROMOTION (
    order_promotion_id UUID PRIMARY KEY,
    promotion_id UUID NOT NULL REFERENCES PROMOTION(promotion_id),
    order_id UUID NOT NULL REFERENCES "ORDER"(order_id)
);


